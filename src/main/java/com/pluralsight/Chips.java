package com.pluralsight;

public class Chips {
    private String type;

    public Chips() {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    @Override
    public String toString() {
        return type + " chips";
}
}
